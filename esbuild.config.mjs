import esbuild from 'esbuild'
import process from 'process'
import fs from 'fs'
import path from 'path'
import os from 'os'
import { builtinModules } from 'node:module'

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`

const prod = process.argv[2] === 'production'
const builtins = [...builtinModules, ...builtinModules.map(name => `node:${name}`)]

/**
 * Resolve @jshph/digest from source (.ts) rather than requiring a pre-built
 * dist/. This way GitHub installs (no dist/) and local file: links both work.
 * esbuild handles the TS→JS compilation during bundling.
 */
function resolveDigestPlugin() {
	return {
		name: 'resolve-digest',
		setup(build) {
			build.onResolve({ filter: /^@jshph\/digest$/ }, args => {
				return { path: require.resolve('@jshph/digest/src/index.ts') }
			})
			// Also resolve sub-path imports like @jshph/digest/core/types
			build.onResolve({ filter: /^@jshph\/digest\// }, args => {
				const sub = args.path.replace('@jshph/digest/', '')
				return { path: require.resolve(`@jshph/digest/src/${sub}`) }
			})
		}
	}
}

function resolveRealtimeBrowserShim() {
	return {
		name: 'resolve-realtime-browser-shim',
		setup(build) {
			build.onResolve({ filter: /^@openai\/agents-realtime\/_shims$/ }, () => {
				const pkg = require.resolve('@openai/agents-realtime/package.json')
				return { path: path.join(path.dirname(pkg), 'dist', 'shims', 'shims-browser.js') }
			})
		}
	}
}

function copyToPluginsPlugin() {
	return {
		name: 'copy-to-plugins',
		setup(build) {
			build.onEnd(async () => {
				const sourceDir = process.cwd()
				const destDir = path.join(
					os.homedir(),
					'obsidian',
					'.obsidian',
					'plugins',
					'reason'
				)
				await fs.promises.mkdir(destDir, { recursive: true })
				for (const file of ['main.js', 'manifest.json', 'styles.css']) {
					const src = path.join(sourceDir, file)
					const dst = path.join(destDir, file)
					try {
						await fs.promises.copyFile(src, dst)
					} catch (err) {
						console.error(`Error copying ${file}: ${err}`)
					}
				}
				console.log(`Copied to ${destDir}`)
			})
		}
	}
}

const context = await esbuild.context({
	platform: 'node',
	banner: { js: banner },
	entryPoints: [{ in: 'src/obsidian.ts', out: 'main' }],
	bundle: true,
	external: [
		'obsidian',
		'electron',
		'@codemirror/autocomplete',
		'@codemirror/collab',
		'@codemirror/commands',
		'@codemirror/language',
		'@codemirror/lint',
		'@codemirror/search',
		'@codemirror/state',
		'@codemirror/view',
		'@lezer/common',
		'@lezer/highlight',
		'@lezer/lr',
		...builtins
	],
	format: 'cjs',
	target: 'es2020',
	logLevel: 'info',
	sourcemap: prod ? false : 'inline',
	treeShaking: true,
	outdir: './',
	plugins: [resolveDigestPlugin(), resolveRealtimeBrowserShim(), copyToPluginsPlugin()]
})

if (prod) {
	await context.rebuild()
	process.exit(0)
} else {
	await context.watch()
}
